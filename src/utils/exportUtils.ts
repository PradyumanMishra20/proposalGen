/**
 * Export content as PDF using browser print functionality
 * @param proposal - Proposal object with title and content
 * @returns Promise<void>
 */
export const exportAsPdf = async (proposal: ProposalData): Promise<void> => {
  try {
    // Generate HTML content
    const htmlContent = generatePdfContent(proposal);
    
    // Create a new window for printing
    const printWindow = window.open('', '_blank');
    
    if (!printWindow) {
      throw new Error('Failed to open print window. Please allow popups and try again.');
    }
    
    // Write content to the new window
    printWindow.document.write(htmlContent);
    printWindow.document.close();
    
    // Wait for content to load, then trigger print
    printWindow.onload = () => {
      setTimeout(() => {
        printWindow.print();
        
        // Close the window after printing (or after user cancels)
        printWindow.onafterprint = () => {
          printWindow.close();
        };
        
        // Fallback timeout to close window
        setTimeout(() => {
          if (!printWindow.closed) {
            printWindow.close();
          }
        }, 1000);
      }, 500);
    };
    
  } catch (error) {
    console.error('Failed to export as PDF:', error);
    throw new Error('Failed to export proposal as PDF');
  }
};

/**
 * Alternative PDF export using html2canvas and jsPDF (if libraries are available)
 * @param proposal - Proposal object with title and content
 * @returns Promise<void>
 */
export const exportAsPdfAdvanced = async (proposal: ProposalData): Promise<void> => {
  try {
    // Check if required libraries are available (they would be loaded dynamically if needed)
    const html2canvas = (window as any).html2canvas;
    const jspdf = (window as any).jspdf;
    
    if (!html2canvas || !jspdf) {
      // Fallback to print method
      return await exportAsPdf(proposal);
    }
    
    // Create a temporary div with the content
    const tempDiv = document.createElement('div');
    tempDiv.innerHTML = generatePdfContent(proposal);
    tempDiv.style.position = 'absolute';
    tempDiv.style.left = '-9999px';
    tempDiv.style.width = '800px';
    document.body.appendChild(tempDiv);
    
    // Convert to canvas
    const canvas = await html2canvas(tempDiv, {
      scale: 2,
      useCORS: true,
      allowTaint: true
    });
    
    // Create PDF
    const pdf = new jspdf.jsPDF({
      orientation: 'portrait',
      unit: 'mm',
      format: 'a4'
    });
    
    const imgData = canvas.toDataURL('image/png');
    const pdfWidth = pdf.internal.pageSize.getWidth();
    const pdfHeight = (canvas.height * pdfWidth) / canvas.width;
    
    pdf.addImage(imgData, 'PNG', 0, 0, pdfWidth, pdfHeight);
    pdf.save(`${proposal.title.replace(/\s+/g, '_').toLowerCase()}.pdf`);
    
    // Clean up
    document.body.removeChild(tempDiv);
    
  } catch (error) {
    console.error('Failed to export as PDF (advanced):', error);
    // Fallback to print method
    return await exportAsPdf(proposal);
  }
};

/**
 * Generate a shareable link for the proposal
 * @param proposal - Proposal object
 * @returns string - Shareable URL
 */
export const generateShareLink = (proposal: ProposalData): string => {
  const baseUrl = window.location.origin;
  const proposalId = Date.now().toString(36);
  const shareUrl = `${baseUrl}/proposal/${proposalId}`;
  
  // Store proposal in localStorage for sharing (in production, this would be a database)
  const sharedProposals: Record<string, ShareLinkData> = JSON.parse(localStorage.getItem('sharedProposals') || '{}');
  sharedProposals[proposalId] = {
    id: proposalId,
    proposal: proposal,
    sharedAt: new Date().toISOString()
  };
  localStorage.setItem('sharedProposals', JSON.stringify(sharedProposals));
  
  return shareUrl;
};

/**
 * Validate proposal content before export
 * @param proposal - Proposal object to validate
 * @returns boolean - Validation result
 */
export const validateProposal = (proposal: ProposalData): boolean => {
  if (!proposal) return false;
  if (!proposal.title || typeof proposal.title !== 'string') return false;
  if (!proposal.content || typeof proposal.content !== 'string') return false;
  if (proposal.content.trim().length === 0) return false;
  
  return true;
};
