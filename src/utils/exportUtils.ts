// Utility functions for exporting proposals

export interface ProposalData {
  title: string;
  content: string;
  date?: string;
}

export interface ShareLinkData {
  id: string;
  proposal: ProposalData;
  sharedAt: string;
}

/**
 * Copy text to clipboard with fallback for older browsers
 * @param text - Text to copy
 * @returns Promise<boolean> - Success status
 */
export const copyToClipboard = async (text: string): Promise<boolean> => {
  try {
    // Modern browsers with clipboard API
    if (navigator.clipboard && window.isSecureContext) {
      await navigator.clipboard.writeText(text);
      return true;
    }
    
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = text;
    textArea.style.position = 'fixed';
    textArea.style.left = '-999999px';
    textArea.style.top = '-999999px';
    document.body.appendChild(textArea);
    textArea.focus();
    textArea.select();
    
    const successful = document.execCommand('copy');
    document.body.removeChild(textArea);
    
    return successful;
  } catch (error) {
    console.error('Failed to copy text:', error);
    throw new Error('Failed to copy text to clipboard');
  }
};

/**
 * Download content as a text file
 * @param content - Text content to download
 * @param filename - Name of the file (without extension)
 * @returns void
 */
export const downloadAsTxt = (content: string, filename: string = 'proposal'): void => {
  try {
    // Create blob with UTF-8 encoding
    const blob = new Blob([content], { type: 'text/plain;charset=utf-8' });
    
    // Create download link
    const url = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = `${filename}.txt`;
    
    // Trigger download
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
    
    // Clean up URL
    URL.revokeObjectURL(url);
  } catch (error) {
    console.error('Failed to download as TXT:', error);
    throw new Error('Failed to download proposal as text file');
  }
};

